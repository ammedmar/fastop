"""Small catalog of finite simplicial complexes."""

from __future__ import annotations

from itertools import combinations, product

from fastop.delta_complex import DeltaComplex
from fastop.simplicial import SimplicialComplex
from fastop.simplicial_set import SimplicialSet


def simplex(dimension: int) -> SimplicialComplex:
    """Return the standard ``dimension``-simplex."""
    _validate_dimension(dimension)
    return SimplicialComplex([tuple(range(dimension + 1))])


def sphere(dimension: int) -> SimplicialComplex:
    """Return the boundary of the standard ``(dimension + 1)``-simplex."""
    _validate_dimension(dimension)
    vertices = tuple(range(dimension + 2))
    return SimplicialComplex(combinations(vertices, dimension + 1))


def real_projective_plane() -> SimplicialComplex:
    """Return Sage's six-vertex triangulation of real projective plane."""
    return SimplicialComplex(_REAL_PROJECTIVE_PLANE_FACETS)


def real_projective_space(dimension: int) -> SimplicialComplex:
    """Return a catalog triangulation of real projective ``dimension``-space.

    This first catalog includes the small Sage triangulations of dimensions
    two and three.
    """
    if dimension == 2:
        return real_projective_plane()
    if dimension == 3:
        return SimplicialComplex(_REAL_PROJECTIVE_3_SPACE_FACETS)
    raise NotImplementedError("only real projective spaces of dimensions 2 and 3 are included")


def complex_projective_plane() -> SimplicialComplex:
    """Return Sage's nine-vertex triangulation of complex projective plane."""
    return SimplicialComplex(_COMPLEX_PROJECTIVE_PLANE_FACETS)


def complex_projective_space(dimension: int) -> SimplicialComplex:
    """Return a catalog triangulation of complex projective ``dimension``-space."""
    _validate_dimension(dimension)
    if dimension == 2:
        return complex_projective_plane()
    if dimension == 3:
        return SimplicialComplex(_COMPLEX_PROJECTIVE_3_SPACE_FACETS)
    raise NotImplementedError("only complex projective spaces of dimensions 2 and 3 are included")


def matching_complex(order: int) -> SimplicialComplex:
    """Return the matching complex of the complete graph on ``order`` vertices.

    Vertices of the returned complex are the edges of the complete graph in
    lexicographic order.  Its facets are the maximum-cardinality matchings.
    """
    if not isinstance(order, int) or isinstance(order, bool):
        raise TypeError("order must be an integer")
    if order < 2:
        raise ValueError("order must be at least two")

    graph_edges = tuple(combinations(range(order), 2))
    edge_to_index = {edge: index for index, edge in enumerate(graph_edges)}
    vertices = tuple(range(order))
    if order % 2:
        matchings = (
            matching
            for omitted in vertices
            for matching in _perfect_matchings(
                tuple(vertex for vertex in vertices if vertex != omitted)
            )
        )
    else:
        matchings = _perfect_matchings(vertices)
    return SimplicialComplex(
        tuple(sorted(edge_to_index[edge] for edge in matching))
        for matching in matchings
    )


def lens_space(dimension: int, order: int = 3) -> DeltaComplex:
    """Return a compact Delta-complex model of a standard lens space.

    The model realizes
    :math:`L^{2m-1}(q)=(S^1)^{*m}/C_q`.  Each circle is a cyclic ``q``-gon,
    and the cyclic group rotates every factor diagonally.
    """
    if not isinstance(dimension, int) or isinstance(dimension, bool):
        raise TypeError("dimension must be an integer")
    if dimension < 1 or dimension % 2 == 0:
        raise ValueError("lens-space dimension must be a positive odd integer")
    if not isinstance(order, int) or isinstance(order, bool):
        raise TypeError("order must be an integer")
    if order < 2:
        raise ValueError("order must be at least two")

    factor_count = (dimension + 1) // 2
    choices = (
        (None,)
        + tuple(("v", index) for index in range(order))
        + tuple(("e", index) for index in range(order))
    )
    labels_by_degree: list[list[tuple]] = [
        [] for _ in range(dimension + 1)
    ]
    for label in product(choices, repeat=factor_count):
        weight = sum(
            0 if choice is None else 1 if choice[0] == "v" else 2
            for choice in label
        )
        if weight:
            labels_by_degree[weight - 1].append(label)
    indices = [
        {label: index for index, label in enumerate(labels)}
        for labels in labels_by_degree
    ]

    face_maps = [tuple(() for _ in labels_by_degree[0])]
    for degree in range(1, dimension + 1):
        face_maps.append(tuple(
            tuple(
                indices[degree - 1][
                    _lens_join_face(label, local_index, order)
                ]
                for local_index in range(degree + 1)
            )
            for label in labels_by_degree[degree]
        ))
    cover = DeltaComplex(face_maps)

    generator = tuple(
        tuple(
            indices[degree][
                tuple(
                    None
                    if choice is None
                    else (choice[0], (choice[1] + 1) % order)
                    for choice in label
                )
            ]
            for label in labels
        )
        for degree, labels in enumerate(labels_by_degree)
    )
    return cover.quotient([generator], require_free=True)


def minimal_simplicial_sphere(dimension: int) -> SimplicialSet:
    """Return the one-vertex simplicial-set model of a positive sphere."""
    return SimplicialSet.minimal_sphere(dimension)


def minimal_simplicial_torus() -> SimplicialSet:
    """Return a one-vertex, six-cell simplicial-set model of the torus."""
    return minimal_simplicial_surface(1)


def minimal_simplicial_surface(genus: int) -> SimplicialSet:
    """Return a compact one-vertex model of an orientable closed surface.

    For positive genus, the standard ``4 * genus``-gon presentation is
    triangulated by a fan before its boundary edges are paired.  The result
    has ``6 * genus - 3`` edges and ``4 * genus - 2`` triangles.
    """
    if not isinstance(genus, int) or isinstance(genus, bool):
        raise TypeError("genus must be an integer")
    if genus < 0:
        raise ValueError("genus must be nonnegative")
    if genus == 0:
        return minimal_simplicial_sphere(2)

    boundary_word = []
    for handle in range(genus):
        a = 2 * handle
        b = a + 1
        boundary_word.extend(((a, 1), (b, 1), (a, -1), (b, -1)))

    polygon_size = len(boundary_word)
    first_diagonal = 2 * genus

    def diagonal(index: int) -> int:
        if index == 1:
            return boundary_word[0][0]
        if index == polygon_size - 1:
            return boundary_word[-1][0]
        return first_diagonal + index - 2

    triangles = []
    for index in range(1, polygon_size - 1):
        boundary_edge, orientation = boundary_word[index]
        left = diagonal(index)
        right = diagonal(index + 1)
        if orientation == 1:
            triangles.append((boundary_edge, right, left))
        else:
            triangles.append((boundary_edge, left, right))

    edge_count = 6 * genus - 3
    return SimplicialSet.from_delta_complex(DeltaComplex([
        [()],
        [(0, 0) for _ in range(edge_count)],
        triangles,
    ]))


def symmetric_product_of_torus(power: int) -> SimplicialSet:
    """Return the ``power``-fold symmetric product of the two-torus."""
    return symmetric_product_of_surface(1, power)


def symmetric_product_of_surface(genus: int, power: int = 3) -> SimplicialSet:
    """Return a symmetric power of the closed orientable genus-``genus`` surface."""
    return minimal_simplicial_surface(genus).symmetric_power(power)


def moore_space(order: int = 3) -> SimplicialComplex:
    """Return a catalog triangulation of the mod-``order`` Moore space."""
    if not isinstance(order, int) or isinstance(order, bool):
        raise TypeError("order must be an integer")
    if order != 3:
        raise NotImplementedError("only the mod-3 Moore space is included")
    return SimplicialComplex(_MOORE_3_SPACE_FACETS)


def _validate_dimension(dimension: int) -> None:
    if not isinstance(dimension, int) or isinstance(dimension, bool):
        raise TypeError("dimension must be an integer")
    if dimension < 0:
        raise ValueError("dimension must be nonnegative")


def _perfect_matchings(vertices: tuple[int, ...]):
    if not vertices:
        yield ()
        return
    first = vertices[0]
    for index in range(1, len(vertices)):
        second = vertices[index]
        remaining = vertices[1:index] + vertices[index + 1 :]
        for matching in _perfect_matchings(remaining):
            yield ((first, second),) + matching


def _lens_join_face(label: tuple, local_index: int, order: int) -> tuple:
    offset = 0
    for factor, choice in enumerate(label):
        width = 0 if choice is None else 1 if choice[0] == "v" else 2
        if local_index >= offset + width:
            offset += width
            continue

        answer = list(label)
        if choice[0] == "v":
            answer[factor] = None
        else:
            edge = choice[1]
            edge_position = local_index - offset
            vertex = (edge + 1) % order if edge_position == 0 else edge
            answer[factor] = ("v", vertex)
        return tuple(answer)
    raise IndexError("local face index is outside the join cell")


